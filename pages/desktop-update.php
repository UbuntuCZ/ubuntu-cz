			<h2>Postup aktualizace</h2>
			<ol>
				<li><strong>Spusťte správce aktualizací</strong><br>Tuto akci provedete například vyhledáním slova "Správce aktualizací" v menu programů. Menu se nachází v dokovacím panelu (standardně vlevo dole).</li>
				<li><strong>Zkontrolujte dostupné aktualizace</strong><br>Po otevření správce aktualizací klikněte na tlačítko "Zkontrolovat". Systém si ověří, které balíky může aktualizovat a uloží si je do databáze.</li>
				<li><strong>Aktualizujte</strong><br>Jakmile je kontrola dokončena, klikněte na tlačítko "Aktualizovat". Zobrazí se poznámky k vydání. Pokud si přejete systém povýšit na novou verzi, pokračujte tlačítkem "Aktualizovat" a držte se aktualizačních pokynů.</li>
			</ol>
			<p><em>Pokud chcete přejít na standardní verzi bez dlouhodobé podpory, může být potřeba ještě povolit aktualizaci na tyto verze v aplikaci "Software &amp; Aktualizace", v záložce "Aktualizace" pod položkou "Zobrazit upozornění na nové verze Ubuntu", kde vyberete "Pro jakoukoliv novou verzi".</em></p>
		</article>
